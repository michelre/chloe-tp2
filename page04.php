<p >Les fréquences sont des nombres calculés. En tant que tels,
ils doivent être le plus souvent arrondis pour faciliter la compréhension du
tableau. L'usage est d'arrondir à la première décimale. Les totaux (effectif
total et fréquence totale de l00%) servent de contrôle.</p>

