<h3>Tableaux</h3>
<p >Le tableau associé à une variable (quelque soit le type de
la variable) est un tableau qui comporte au départ trois lignes.</p>

<p >Sur la première on fait figurer les différentes valeurs
prises par la variable. Lorsqu'elles peuvent être ordonnées (variable
qualitative ordonnée ou variable numérique) les valeurs sont présentées hiérarchisées.</p>

<p >Sur la deuxième ligne on fait figurer le nombre d'individus
de la population qui possèdent cette valeur. Cette indication numérique
s'appelle l'effectif de la valeur correspondante. Une case spéciale est réservée
à la fin de cette ligne. C'est celle où l'on indique l'effectif total de la
population.</p>

<p >Sur la troisième ligne on fait figurer le pourcentage que
représente l'effectif de la valeur considérée par rapport à l'effectif total
C'est la fréquence de la valeur considérée. En dessous de la case réservée à l'effectif
total on a l'habitude d'indiquer 100%, qui doit être la somme des fréquences,
comme la somme des effectifs doit être l'effectif total.</p>

<p >La liste des valeurs de la variable, associée à celle des
effectifs définit ce que l'on appelle la distribution de la variable sur la
population étudiée.</p>

