<em>Le but de cet exercice est de décrire et éventuellement comparer deux textes.</em>
<h2>Exercice à réaliser:</h2>
<p>Choisir sur internet deux extraits de texte, l'un scientifique, l'autre non,
(par exemple un extrait de roman ou une recette de cuisine...)
d'une dizaine de lignes.</p>

<p>On choisit comme variable la variable numérique "taille du mot".
Par exemple si le premier texte comprend 300 mots, vous devez compter  le
mombre de mots d'une lettre, de deux lettres etc...</p>

<p>Répondez aux questions suivantes,</p>
<ul>
<li>Dans notre exemple, identifiez la population et les individus étudiés.</li>
<li>Combien d'individus sont concernés par notre étude, combien de populations?  </li>
<li>Proposez un variable quantitative, une variable qualitative et une variable
logique permettant de décrire les individus que vous avez identifiés.</li>
</ul>
<p>Reprenons notre variable "taille du mot"</p>
<ul>
<li>Dressez les tableaux correspondants aux deux distributions,</li>
<li>Tracez les diagrammes, décrivez les deux séries et comparez. </li>
</ul>
