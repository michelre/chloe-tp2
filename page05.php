<p >Une description de la série numérique s'appuyant sur le
diagramme construit peut être :   On observe une relative concentration des
textes les plus courts opposée à une toute aussi relative dispersion des textes
les plus longs. On observe également une nette asymétrie de la distribution
vers les valeurs les plus basses. Enfin la courbe formée par les extrémités,
des bâtons du diagramme est tout d'abord croissante jusqu'à la valeur 36
d'effectif 8 puis décroissante. La croissance est relativement forte la décroissance
au contraire plutôt douce.</p>

<p >Toutes ces observations vont dans le même sens à savoir une
opposition entre la relative homogénéité des textes <em> courts </em>
composés par un grand nombre d'élèves et l'hétérogénéité des textes <em>
longs </em>, composés par un nombre moindre d'élèves.   Histogrammes.</p>

<p >On désigne par ce nom les représentations graphiques où
l'effectif de chacune des valeurs de la variable est visualisé par un
rectangle.</p>

<p >La seule règle de construction de ces rectangles est que
l'aire d'un rectangle doit être proportionnelle à l'effectif (ou à la
fréquence). Si comme c'est souvent le cas les rectangles sont construits sur du
papier millimétré/quadrillé à petits carreaux, cette règle exige de commencer
par déterminer le nombre d'individus que représente 1 cm²/ un petit carreau.</p>

<p >Premier cas : variables autres que numériques.</p>

<p >Dans ce cas, ta construction des rectangles représentant les
effectifs de chacune des valeurs prises est simple. On choisit l'échelle
c'est-à-dire le nombre d'individus que représente un petit carreau puis des
rectangles de même base. La hauteur du rectangle est alors proportionnelle à
l'effectif de la catégorie ainsi qu'à la fréquence. L'oeil n'a plus qu'à
s'attacher aux hauteurs des rectangles.</p>

<p >Deuxième cas : variables numériques présentées sous forme
d'intervalles.</p>

<p >Dans ce cas, la construction des rectangles est plus
complexe. En effet la base de chaque rectangle est imposée (elle se confond
avec l'intervalle numérique). Par conséquent un calcul va être nécessaire pour
déterminer la hauteur de chacun des rectangles.</p>

<p >Il faut calculer les hauteurs des rectangles, en effectuant
le quotient des aires par les bases correspondantes.</p>