<h3>Représentations graphiques</h3>
<p >La deuxième étape du processus descriptif est celui du tracé
d'une représentation graphique de la distribution de la variable. Nous ne décrirons
ici que quelques techniques de représentation graphique.</p>

<h4>Diagrammes circulaires.</h4>

<p >Les diagrammes circulaires se présentent sous la forme d'un
disque dont les secteurs ont une mesure dont le pourcentage par rapport à celle
du secteur total (360°) est égal à la fréquence de chacune des valeurs de la
variable.</p>

