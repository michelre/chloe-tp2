<h4 >Diagrammes</h4>

<p >Les diagrammes en bâton sont les représentations graphiques
les plus simples â tracer et à décrire.</p>

<p >Ces diagrammes en bâton sont constitués de bâtons comme leur
nom l'indique. Chaque trait est associé à une des valeurs de la variable. Ces
bâtons sont de longueur proportionnelle à l'effectif (ou à la fréquence de la
valeur représentée.</p>

<p >Si la variable n'est pas numérique les bâtons sont disposés
à intervalles réguliers sur une droite horizontale et cette disposition n'est
adoptée que parce qu'elle favorise l'appréhension visuelle.</p>

<p >Si la variable est numérique, on porte sur l'axe horizontal
les valeurs de la variable. Les bâtons s'élèvent au dessus des valeurs dont ils
représentent les effectifs.</p>

<p >La description d'un tel diagramme (dans le cas où la
variable est numérique) s'appuie sur les relevés visuels suivants :</p>

<p >Identification des éventuelles concentrations d'effectifs
C'est-à-dire des zones du dessin où les traits sont contiguës et élevés.
Réciproquement, on détermine les creux d'effectifs, c'est-à-dire les zones du
dessin où les traits sont clairsemées et peu élevés.</p>

<p >La tache que représente l'ensemble des bâtons peut être
symétrique ou encore asymétrique.</p>

<p >La courbe qu'esquissent les extrémités des bâtons peut être
croissante ou décroissante.</p>

