<h3>Variables ou caractères statistiques.</h3>

<p>La description statistique d'une population d'objets exige
des opérations différentes selon que cette démarche s'inscrit dans une
problématique de recherche bien définie ou n'est
qu'une démarche exploratoire. Si l'on souhaite apporter des réponses à des
questions déjà posées (par exemple étudier l'hémotypologie d'une population de
167 Kurdes) les caractéristiques sélectionnées pour décrire la, population sont
de fait imposées (dans le cas cité, il s'agit de caractéristiques telles que le
rhésus le groupe sanguin etc.) En revanche si la démarche est avant tout
exploratoire les caractéristiques pertinentes pour décrire les objets de la
population ne sont pas toujours disponibles a priori. Le premier souci du statisticien
est alors d'isoler les informations recueillies qui sont susceptibles de
variations entre les objets étudiés.</p>

<h5>Donnons un exemple. </h5>

<p>La population que nous nous proposons d'explorer est une
population composée de trois individus, Ces trois individus sont trois textes.
Ce sont trois extraits du même conte: Blanche-Neige.
Ils reprennent le début du conte et s'interrompent à l'instant où le miroir
magique déclare que Blanche-Neige est plus belle que
la reine.</p>

<h5>Extrait 1 :</h5>
<p class=cursive>
 Dans un château vivaient une jolie
princesse, Blanche-Neige, et une reine cruelle et
très belle. &quot;Vous êtes la plus belle du royaume&quot; lui disait son
miroir magique.<br />
Mais un jour qu'elle lui demandait: &quot;Suis-je toujours la plus belle ?&quot;
 le miroir lui répondit: &quot;Non ma reine, c'est Blanche-Neige.&quot; 
</p>
<h5>Extrait 2</h5>
<p class=arial>
Il était une fois par un jour d'hiver une reine qui cousait
auprès d'une fenêtre en bois d'ébène. Dehors, il neigeait. La reine ouvrit la
fenêtre pour regarder danser les flocons et elle se piqua avec son aiguille.
Quelques gouttes de sang tombèrent dans la neige. C'était si joli ce rouge sur
le blanc que la reine dit: &quot;Je voudrais un enfant qui ait la peau blanche
comme la neige, les lèvres et les joues rouges comme le sang et les cheveux
noirs comme l'ébène.&quot; On l'appela Blanche-Neige. Mais
la reine tomba malade et mourut. Le roi se remaria avec une femme très belle
mais très orgueilleuse. Elle possédait un miroir magique et chaque matin elle
se regardait et demandait: &quot;Miroir, miroir magique, dis moi qui est la
plus belle ?&quot;. Et le miroir répondait: &quot;Ô reine, tu es la plus belle
en ce royaume.&quot; Mais Blanche-Neige grandissait
et devenait de plus en plus belle. Un jour, le miroir répondit &quot;Ô reine, tu
es très belle, mais Blanche-Neige est la plus belle en ce royaume&quot;.
</p>
<h5>Extrait 3.</h5>
<p class=monospace>
Il était une fois une reine cruelle qui passait
son temps à interroger son miroir magique. &quot;Miroir, miroir joli, lui
disait-elle: qui est la plus belle du pays?&quot;. Et toujours le miroir lui
répondait: &quot;Vous êtes la plus belle!&quot; Un jour son miroir osa lui dire
: &quot;Ô reine, vous êtes belle, mais Blanche-Neige
l'est encore plus que vous&quot;.
</p>
<p>Ces trois objets sont différents et le premier travail
statistique est de rendre compte de ces différences en créant des variables ou
caractères statistiques.</p>

<p>Ainsi les trois textes diffèrent selon leur longueur. Nous
traduirons cette différence en créant la variable  <em>nombre de mots</em> .
Au premier texte correspond la valeur 50, au second 180, et au troisième 60.</p>

<p>Ils diffèrent selon le style utilisé par l'imprimeur. Ces
variations seront traduites par une variable nommée  <em>style</em> . Le
premier texte est de style  <em>cursive</em> , le second  <em>arial</em> , le
troisième  <em>monospace</em> .</p>

<p>Ils diffèrent aussi par exemple par la délivrance de
l'information des liens unissant Blanche- Neige et la reine. On peut ainsi
créer une variable nommée  <em>Oppositions entre Blanche-Neige
et la reine</em>  qui prendrait les valeurs suivantes
 <em>princesse/reine</em>  pour le premier texte,  <em>Belle-fille
princesse/Belle-mère reine</em>  pour le second,  <em>aucune</em>  pour le
troisième.</p>

<p>Ils diffèrent enfin par la mise en scène du roi ou l'absence
de ce personnage. On peut alors créer une variable <em>roi personnage
cité</em> qui prend deux valeurs <em>vrai</em> ou <em>faux</em> . Il existe
encore bien d'autres variables à créer (par exemple le nombre de personnages)
mais nous ne travaillerons qu'avec les quatre ainsi construites. En effet elles
relèvent de quatre traitements statistiques différents et seront dites de types
différents.</p>

<p>Les quatre variables isolées plus haut sont de types
différents car les catégorisations qu'elles permettent d'effectuer sur les
individus de la population sont de nature différente.</p>

<ol><li>La variable  <em>roi personnage cité</em>  qui ne prend
que deux valeurs vrai/faux, permet de catégoriser en deux classes les
individus. Deux individus quelconques de la population ont donc soit des
valeurs identiques soit des valeurs contraires. Ils sont soit identifiées (on
leur associe la même valeur) soit opposés (on leur associe deux valeurs
contraires). Ce type de variable s'appelle <span style='font-weight: bold'>
variable binaire ou variable
logique.</span></li>

<li>La variable  <em>style</em>  permet une catégorisation
des individus qui ne permet pas davantage de penser des individus en terme de
 <em> proches </em>  de  <em> voisins </em>  ou de  <em> éloignés </em>  En
effet aucune hiérarchie ne peut être établie entre les trois classes que sont
 <em>style1</em>  , <em>style 2</em> ,  <em>style 3</em> . Les individus de
la population une fois triés, sont soit identifiés (on leur associe la même
valeur) soit différents (on leur associe des valeurs différentes). Une telle
variable est dite qualitative. D'autres exemples de variables qualitatives
sont: le groupe sanguin (A, AB, O etc.), le sexe (homme, femme) etc. Les
variables binaires sont des variables qualitatives particulières.</li>

<li>La variable  <em>oppositions Blanche-Neige/
reine</em>  est une variable un peu différente de la variable  <em>style</em> 
car les classes qui lui sont attachées peuvent être ordonnées, hiérarchisées.
On peut ordonner les trois classes produites selon l'ordre suivant:
 <em>aucune information</em> ,  <em>princesse/reine</em> ,  <em>Belle-fille
princesse/belle-mère reine</em>  qui rend compte de la progression du niveau
d'information sur les liens entre Blanche- Neige et la reine. Par conséquent
les individus de la population une fois catégorisés peuvent être déclarés
identiques, différents comme dans le cas précédent mais aussi plus proche de,
plus éloigné de, etc. Ce type de variable est dit <span style='font-weight: bold'>
qualitative ordonnée</span>.</li>

<li>Enfin la variable
 <em>nombre de mots</em>  est une variable qui permet de penser l'écart entre
deux individus auxquels on associe des valeurs différentes pour cette variable.
Non seulement on peut ordonner les individus à l'aide des valeurs prises (le
texte l comprend moins de mots que le texte 3 et encore moins que le texte 2)
mais on peut également comparer les individus par le rapport entre les valeurs
prises (le texte 2 comprend trois fois plus de mots que le texte 3). Cette
variable se dénomme <span style='font-weight: bold'>variable
numérique ou quantitative</span>.</li>
</ol>

<p><span style='font-weight: bold'>En définitive, deux grandes
catégories de variables sont à retenir: les variables numériques ou
quantitative et les variables qualitatives</span></p>

<p>Une fois les variables identifiées le travail descriptif
commence. On doit tout d'abord procéder à l'identification de la valeur prise
par chaque individu. Cette identification produit une liste de valeurs prises,
liste désordonnée, qui est difficilement déchiffrable. Cette liste s'appelle
aussi série</p>

<p>Par exemple on obtient les liste ou les séries suivantes:</p>

<p>Roi personnage cité : faux, vrai, faux. Nombre de mots : 50,
180, 60.</p>

<p>La deuxième étape de ce travail et donc la première description
consiste à produire un tableau décrivant les variations de la variable, à
produire une ou plusieurs représentations graphiques et à mettre en mots les
informations ainsi recueillies.</p>
